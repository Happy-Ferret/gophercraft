package packet

const (
	MOVEMENTFLAG_NONE                 uint32 = 0x00000000
	MOVEMENTFLAG_FORWARD              uint32 = 0x00000001
	MOVEMENTFLAG_BACKWARD             uint32 = 0x00000002
	MOVEMENTFLAG_STRAFE_LEFT          uint32 = 0x00000004
	MOVEMENTFLAG_STRAFE_RIGHT         uint32 = 0x00000008
	MOVEMENTFLAG_LEFT                 uint32 = 0x00000010
	MOVEMENTFLAG_RIGHT                uint32 = 0x00000020
	MOVEMENTFLAG_PITCH_UP             uint32 = 0x00000040
	MOVEMENTFLAG_PITCH_DOWN           uint32 = 0x00000080
	MOVEMENTFLAG_WALKING              uint32 = 0x00000100 // Walking
	MOVEMENTFLAG_ONTRANSPORT          uint32 = 0x00000200 // Used for flying on some creatures
	MOVEMENTFLAG_DISABLE_GRAVITY      uint32 = 0x00000400 // Former MOVEMENTFLAG_LEVITATING. This is used when walking is not possible.
	MOVEMENTFLAG_ROOT                 uint32 = 0x00000800 // Must not be set along with MOVEMENTFLAG_MASK_MOVING
	MOVEMENTFLAG_FALLING              uint32 = 0x00001000 // damage dealt on that type of falling
	MOVEMENTFLAG_FALLING_FAR          uint32 = 0x00002000
	MOVEMENTFLAG_PENDING_STOP         uint32 = 0x00004000
	MOVEMENTFLAG_PENDING_STRAFE_STOP  uint32 = 0x00008000
	MOVEMENTFLAG_PENDING_FORWARD      uint32 = 0x00010000
	MOVEMENTFLAG_PENDING_BACKWARD     uint32 = 0x00020000
	MOVEMENTFLAG_PENDING_STRAFE_LEFT  uint32 = 0x00040000
	MOVEMENTFLAG_PENDING_STRAFE_RIGHT uint32 = 0x00080000
	MOVEMENTFLAG_PENDING_ROOT         uint32 = 0x00100000
	MOVEMENTFLAG_SWIMMING             uint32 = 0x00200000 // appears with fly flag also
	MOVEMENTFLAG_ASCENDING            uint32 = 0x00400000 // press "space" when flying
	MOVEMENTFLAG_DESCENDING           uint32 = 0x00800000
	MOVEMENTFLAG_CAN_FLY              uint32 = 0x01000000 // Appears when unit can fly AND also walk
	MOVEMENTFLAG_FLYING               uint32 = 0x02000000 // unit is actually flying. pretty sure this is only used for players. creatures use disable_gravity
	MOVEMENTFLAG_SPLINE_ELEVATION     uint32 = 0x04000000 // used for flight paths
	MOVEMENTFLAG_SPLINE_ENABLED       uint32 = 0x08000000 // used for flight paths
	MOVEMENTFLAG_WATERWALKING         uint32 = 0x10000000 // prevent unit from falling through water
	MOVEMENTFLAG_FALLING_SLOW         uint32 = 0x20000000 // active rogue safe fall spell (passive)
	MOVEMENTFLAG_HOVER                uint32 = 0x40000000 // hover cannot jump

	MOVEMENTFLAG_MASK_MOVING uint32 = MOVEMENTFLAG_FORWARD | MOVEMENTFLAG_BACKWARD | MOVEMENTFLAG_STRAFE_LEFT | MOVEMENTFLAG_STRAFE_RIGHT |
		MOVEMENTFLAG_FALLING | MOVEMENTFLAG_FALLING_FAR | MOVEMENTFLAG_ASCENDING | MOVEMENTFLAG_DESCENDING |
		MOVEMENTFLAG_SPLINE_ELEVATION

	MOVEMENTFLAG_MASK_TURNING uint32 = MOVEMENTFLAG_LEFT | MOVEMENTFLAG_RIGHT | MOVEMENTFLAG_PITCH_UP | MOVEMENTFLAG_PITCH_DOWN

	MOVEMENTFLAG_MASK_MOVING_FLY uint32 = MOVEMENTFLAG_FLYING | MOVEMENTFLAG_ASCENDING | MOVEMENTFLAG_DESCENDING

	/// @todo if needed: add more flags to this masks that are exclusive to players
	MOVEMENTFLAG_MASK_PLAYER_ONLY uint32 = MOVEMENTFLAG_FLYING

	/// Movement flags that have change status opcodes associated for players
	MOVEMENTFLAG_MASK_HAS_PLAYER_STATUS_OPCODE uint32 = MOVEMENTFLAG_DISABLE_GRAVITY | MOVEMENTFLAG_ROOT |
		MOVEMENTFLAG_CAN_FLY | MOVEMENTFLAG_WATERWALKING | MOVEMENTFLAG_FALLING_SLOW | MOVEMENTFLAG_HOVER

	MOVEMENTFLAG2_NONE                  uint32 = 0x00000000
	MOVEMENTFLAG2_NO_STRAFE             uint32 = 0x00000001
	MOVEMENTFLAG2_NO_JUMPING            uint32 = 0x00000002
	MOVEMENTFLAG2_UNK3                  uint32 = 0x00000004 // Overrides various clientside checks
	MOVEMENTFLAG2_FULL_SPEED_TURNING    uint32 = 0x00000008
	MOVEMENTFLAG2_FULL_SPEED_PITCHING   uint32 = 0x00000010
	MOVEMENTFLAG2_ALWAYS_ALLOW_PITCHING uint32 = 0x00000020
	MOVEMENTFLAG2_UNK7                  uint32 = 0x00000040
	MOVEMENTFLAG2_UNK8                  uint32 = 0x00000080
	MOVEMENTFLAG2_UNK9                  uint32 = 0x00000100
	MOVEMENTFLAG2_UNK10                 uint32 = 0x00000200
	MOVEMENTFLAG2_INTERPOLATED_MOVEMENT uint32 = 0x00000400
	MOVEMENTFLAG2_INTERPOLATED_TURNING  uint32 = 0x00000800
	MOVEMENTFLAG2_INTERPOLATED_PITCHING uint32 = 0x00001000
	MOVEMENTFLAG2_UNK14                 uint32 = 0x00002000
	MOVEMENTFLAG2_UNK15                 uint32 = 0x00004000
	MOVEMENTFLAG2_UNK16                 uint32 = 0x00008000
)
